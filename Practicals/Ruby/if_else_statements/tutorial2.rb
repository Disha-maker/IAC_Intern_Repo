  # if statement example
x = 4
if x > 3
  puts "x is greater than 3"
  puts "running this code block (puts)"
end

  # else statement example
x = 2
if x > 3
  puts "x is greater than 3"
  puts "executing lines of code (puts)"
else
  puts "other code block here now"
  puts "condition (x > 3) is not true"
end